// Useful string methods

// add spacing for length
let myName = "    WaqasKhan   ";

console.log(myName.length);

// trim the Name
myName = myName.trim();
console.log(myName);

// gives the length without spacing
console.log(myName.length);

// toUpperCase

myName = myName.toUpperCase();
console.log(myName);

// toLowerCase

myName = myName.toLowerCase();
console.log(myName);

// SliceIndex

let newString = myName.slice(0, 5);
console.log(newString);
