//  == vs ===

// it will check only value
let num3 = "6";
let num4 = 6;
console.log(num3 == num4);

// it will check datatype and value

let num5 = "5";
let num6 = "5";
console.log(num5 === num6);

// != vs !==
console.log(num5 != num6);
console.log(num5 !== num6);
